
package DTO;

public class TelaDTO {
    private String nome_produto, descri_produto, preco_produto;

    public String getNome_produto() {
        return nome_produto;
    }

    public void setNome_produto(String nome_produto) {
        this.nome_produto = nome_produto;
    }

    public String getDescri_produto() {
        return descri_produto;
    }

    public void setDescri_produto(String descri_produto) {
        this.descri_produto = descri_produto;
    }

    public String getPreco_produto() {
        return preco_produto;
    }

    public void setPreco_produto(String preco_produto) {
        this.preco_produto = preco_produto;
    }
    
}
